#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*a;
	char	d;

	a = (char*)b;
	d = (char)c;
	while (len)
	{
		*a = d;
		len--;
		a++;
	}
	return (b);
}
